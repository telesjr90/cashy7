export function loadRootEnvFile() {
  applyEnvMap(parseEnvFile(path.join(process.cwd(), ".env")));
}

export function loadSmokeEnvFile() {
  applyEnvMap(parseEnvFile(SMOKE_ENV_FILE));
}

export function ensureTmpDir() {
  fs.mkdirSync(TMP_DIR, { recursive: true });
}

export function writeSmokeEnvFile(values) {
  ensureTmpDir();
  const lines = [
    "# Generated by scripts/seed-cross-household-privacy-test-users.mjs — do not commit",
    `CASHFLOW_HOUSEHOLD_A_OWNER_EMAIL=${values.householdAOwnerEmail}`,
    `CASHFLOW_HOUSEHOLD_A_OWNER_PASSWORD=${values.householdAOwnerPassword}`,
    `CASHFLOW_HOUSEHOLD_B_OWNER_EMAIL=${values.householdBOwnerEmail}`,
    `CASHFLOW_HOUSEHOLD_B_OWNER_PASSWORD=${values.householdBOwnerPassword}`,
    `CASHFLOW_HOUSEHOLD_A_MEMBER_EMAIL=${values.householdAMemberEmail}`,
    `CASHFLOW_HOUSEHOLD_A_MEMBER_PASSWORD=${values.householdAMemberPassword}`,
    `CASHFLOW_E2E_BASE_URL=${values.baseUrl}`,
    "",
  ];
  fs.writeFileSync(SMOKE_ENV_FILE, lines.join("\n"), "utf8");
}

export function writeSeedMetadata(metadata) {
  ensureTmpDir();
  fs.writeFileSync(SEED_METADATA_FILE, `${JSON.stringify(metadata, null, 2)}\n`, "utf8");
}

export function readSeedMetadata() {
  if (!fs.existsSync(SEED_METADATA_FILE)) {
    return null;
  }
  return JSON.parse(fs.readFileSync(SEED_METADATA_FILE, "utf8"));
}

export function assertCrossHouseholdSeedAllowed() {
  if (process.env.CASHFLOW_ALLOW_CROSS_HOUSEHOLD_TEST_SEED !== "YES") {
    throw new Error(
      "Refusing to seed cross-household test users. Set CASHFLOW_ALLOW_CROSS_HOUSEHOLD_TEST_SEED=YES to opt in."
    );
  }
}

export function assertCrossHouseholdCleanupAllowed() {
  if (process.env.CASHFLOW_CONFIRM_CROSS_HOUSEHOLD_TEST_CLEANUP !== "YES") {
    return false;
  }
  return true;
}

export function requireSupabaseConfig() {
  loadRootEnvFile();

  const supabaseUrl = process.env.VITE_SUPABASE_URL?.trim();
  const serviceRoleKey = process.env.SUPABASE_SERVICE_ROLE_KEY?.trim();
  const anonKey = process.env.VITE_SUPABASE_ANON_KEY?.trim();

  if (!supabaseUrl) {
    throw new Error("Missing VITE_SUPABASE_URL. Add it to .env or the environment.");
  }

  if (!serviceRoleKey) {
    throw new Error(
      "Missing SUPABASE_SERVICE_ROLE_KEY. Add it to .env or the environment for trusted seed scripts only."
    );
  }

  return { supabaseUrl, serviceRoleKey, anonKey };
}

export function requireAnonConfig() {
  loadRootEnvFile();
  loadSmokeEnvFile();

  const supabaseUrl = process.env.VITE_SUPABASE_URL?.trim();
  const anonKey = process.env.VITE_SUPABASE_ANON_KEY?.trim();

  if (!supabaseUrl || !anonKey) {
    throw new Error(
      "Missing VITE_SUPABASE_URL or VITE_SUPABASE_ANON_KEY for privacy smoke assertions."
    );
  }

  return { supabaseUrl, anonKey };
}

