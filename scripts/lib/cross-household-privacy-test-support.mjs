export async function createAuthenticatedClient(supabaseUrl, anonKey, email, password) {
  const client = createClient(supabaseUrl, anonKey, {
    auth: { autoRefreshToken: false, persistSession: false },
  });

  const { error } = await client.auth.signInWithPassword({ email, password });
  if (error) {
    throw new Error(`Could not sign in ${email}: ${error.message}`);
  }

  return client;
}

export async function countRows(client, table, filters = {}) {
  let query = client.from(table).select("id", { count: "exact", head: true });
  for (const [column, value] of Object.entries(filters)) {
    query = query.eq(column, value);
  }
  const { count, error } = await query;
  if (error) {
    throw new Error(`countRows(${table}) failed: ${error.message}`);
  }
  return count ?? 0;
}

export async function fetchRowById(client, table, id, columns = "id") {
  const { data, error } = await client.from(table).select(columns).eq("id", id).maybeSingle();
  if (error) {
    throw new Error(`fetchRowById(${table}, ${id}) failed: ${error.message}`);
  }
  return data;
}

export function isSafeRpcErrorMessage(message) {
  const normalized = String(message ?? "").toLowerCase();
  if (!normalized) {
    return true;
  }
  if (normalized.includes("service_role") || normalized.includes("service role")) {
    return false;
  }
  if (/eyJ[a-zA-Z0-9_-]{10,}/.test(message)) {
    return false;
  }
  return true;
}

