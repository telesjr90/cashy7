export function parseEnvFile(filePath) {
  if (!fs.existsSync(filePath)) {
    return {};
  }

  const parsed = {};
  const lines = fs.readFileSync(filePath, "utf8").split(/\r?\n/);

  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) {
      continue;
    }

    const separatorIndex = trimmed.indexOf("=");
    if (separatorIndex === -1) {
      continue;
    }

    const key = trimmed.slice(0, separatorIndex).trim();
    let value = trimmed.slice(separatorIndex + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }

    parsed[key] = value;
  }

  return parsed;
}

export function applyEnvMap(envMap) {
  for (const [key, value] of Object.entries(envMap)) {
    if (value && !process.env[key]) {
      process.env[key] = value;
    }
  }
}

export function loadRootEnvFile() {
  applyEnvMap(parseEnvFile(path.join(process.cwd(), ".env")));
}

export function loadSmokeEnvFile() {
  applyEnvMap(parseEnvFile(SMOKE_ENV_FILE));
}

export function ensureTmpDir() {
  fs.mkdirSync(TMP_DIR, { recursive: true });
}

export function writeSmokeEnvFile(values) {
  ensureTmpDir();
  const lines = [
    "# Generated by scripts/seed-two-user-privacy-test-users.mjs — do not commit",
    `CASHFLOW_OWNER_EMAIL=${values.ownerEmail}`,
    `CASHFLOW_OWNER_PASSWORD=${values.ownerPassword}`,
    `CASHFLOW_MEMBER_EMAIL=${values.memberEmail}`,
    `CASHFLOW_MEMBER_PASSWORD=${values.memberPassword}`,
    `CASHFLOW_E2E_BASE_URL=${values.baseUrl}`,
    "",
  ];
  fs.writeFileSync(SMOKE_ENV_FILE, lines.join("\n"), "utf8");
}

export function writeSeedMetadata(metadata) {
  ensureTmpDir();
  fs.writeFileSync(SEED_METADATA_FILE, `${JSON.stringify(metadata, null, 2)}\n`, "utf8");
}

export function readSeedMetadata() {
  if (!fs.existsSync(SEED_METADATA_FILE)) {
    return null;
  }
  return JSON.parse(fs.readFileSync(SEED_METADATA_FILE, "utf8"));
}

export function generatePassword() {
  return crypto.randomBytes(24).toString("base64url");
}

export function assertSeedAllowed() {
  if (process.env.CASHFLOW_ALLOW_TEST_USER_SEED !== "YES") {
    throw new Error(
      "Refusing to seed test users. Set CASHFLOW_ALLOW_TEST_USER_SEED=YES to opt in."
    );
  }
}

export function assertTestEmail(email, label) {
  const normalized = email.trim().toLowerCase();
  const looksLikeTestEmail =
    normalized.endsWith("@example.com") ||
    normalized.includes(".test") ||
    normalized.includes("+e2e@");

  if (!looksLikeTestEmail) {
    throw new Error(
      `${label} must use an obvious test email (@example.com, .test, or +e2e).`
    );
  }
}

export function requireSupabaseConfig() {
  loadRootEnvFile();

  const supabaseUrl = process.env.VITE_SUPABASE_URL?.trim();
  const serviceRoleKey = process.env.SUPABASE_SERVICE_ROLE_KEY?.trim();

  if (!supabaseUrl) {
    throw new Error("Missing VITE_SUPABASE_URL. Add it to .env or the environment.");
  }

  if (!serviceRoleKey) {
    throw new Error(
      "Missing SUPABASE_SERVICE_ROLE_KEY. Add it to .env or the environment for trusted seed scripts only."
    );
  }

  return { supabaseUrl, serviceRoleKey };
}

